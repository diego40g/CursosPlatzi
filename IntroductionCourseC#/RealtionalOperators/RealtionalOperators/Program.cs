Console.WriteLine("Operadores Relacionales");

int value1 = 3;
int value2 = 18;
int value3 = -21;

// == igual a
Console.WriteLine("Resultado de "+value1+"=="+value2+": "+(value1==value2));

// != no igual a
Console.WriteLine("Resultado de " + value1 + "!=" + value2 + ": " + (value1 !=value2));

// > mayor que
Console.WriteLine("Resultado de " + value1 + ">" + value2 + ": " + (value1 > value2));

// >= mayor o igual que
Console.WriteLine("Resultado de " + value2 + ">=" + value3 + ": " + (value2 >= value3));

// < menor que
Console.WriteLine("Resultado de " + value2 + "<" + value3 + ": " + (value2 < value3));

// <= menor igual que
Console.WriteLine("Resultado de " + value2 + "<=" + value3 + ": " + (value2 <= value3));