Console.WriteLine("Operadores Relacionales");

int value1 = 3;
int value2 = 18;
int value3 = -21;

// == igual a
Console.WriteLine(value1==value2);

// != no igual a
Console.WriteLine(value1!=value2);

// > mayor que
Console.WriteLine(value1 > value2);

// >= mayor o igual que
Console.WriteLine(value2 >= value3);

// < menor que
Console.WriteLine(value2 < value3);

// <= menor igual que
Console.WriteLine(value2 <= value3);